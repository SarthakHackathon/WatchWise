export const searchFilms = (query) => films.filter(f =>
  f.title.toLowerCase().includes(query.toLowerCase()) ||
  f.director.toLowerCase().includes(query.toLowerCase()) ||
  f.genres.some(g => g.toLowerCase().includes(query.toLowerCase()))
);

export const filterByGenre = (genre) => genre === 'All' ? films : films.filter(f => f.genres.includes(genre));

export const filterByMood = (mood) => mood === 'All' ? films : films.filter(f => f.mood.includes(mood));

export const getAIPicks = (userGenres = [], userMoods = []) => {
  if (userGenres.length === 0 && userMoods.length === 0) return films.slice(0, 6);
  return films.filter(f =>
    f.genres.some(g => userGenres.includes(g)) ||
    f.mood.some(m => userMoods.includes(m))
  ).slice(0, 6);
};
